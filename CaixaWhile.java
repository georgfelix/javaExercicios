package com.java;

public class CaixaWhile {
	
	public static void main(String[] args) {
		
		int qtdProdutos = 5;
		int registro = 0;
		int numero = 0;
		
		while (registro < qtdProdutos) {
			registro++;
			System.out.println("O Produto numero " + registro + " foi registrado");
			
		while (numero <10) {
			numero = numero +1;
			System.out.println(numero);
		}
					
		}
	}

	
}
